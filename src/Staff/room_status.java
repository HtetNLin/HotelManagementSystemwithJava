package Staff;

public class room_status {

	String cin,cout;

	public room_status(String cin, String cout) {
		super();
		this.cin = cin;
		this.cout = cout;
	}

	public String getCin() {
		return cin;
	}

	public void setCin(String cin) {
		this.cin = cin;
	}

	public String getCout() {
		return cout;
	}

	public void setCout(String cout) {
		this.cout = cout;
	}
	
	
}
